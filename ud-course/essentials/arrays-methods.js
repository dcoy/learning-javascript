var points = [25, 16, 7, 9, 31];

console.log(points);

// Access element in an array
console.log(points[0]);

// Add element to the array
points.push(8);
console.log(points);

// Pop off the last element in an array
var last = points.pop();
console.log(last);

// Get length of an array
console.log(points.length);